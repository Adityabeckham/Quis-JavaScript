let hariUjian = 3 ;

switch (hariUjian) {
    case 1:
        console.log("Hari senin");
        break;
    case 2:
        console.log("Hari selasa");
        break;
    case 3:
        console.log("Hari rabu");
        break;
    case 4:
        console.log("Hari kamis");
        break;
    case 5:
        console.log("Hari jumat");
        break;
    case 6:
        console.log("Hari sabtu");
        break;
    case 7:
        console.log("Hari minggu");
        break;
    default:
        console.error("Hari tidak valid , silakan masukkan angka 1-7");
        break;
}

